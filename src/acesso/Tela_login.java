    private void btEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEntrarActionPerformed

        String matricula = txtLogin.getText();
        String senha = new String(txtSenha.getPassword());

        if (!matricula.isEmpty() && !senha.isEmpty()) {

            Usuario usuario = UsuarioDAO.getInstance().buscarUsuario(matricula, senha);
            if (usuario != null) {
                GerenciadorAcesso.getInstance().setLogged(true);

                if (GerenciadorAcesso.getInstance().isLogged()) {
                    if (view != null) {
                        view.dispose();
                    }
                }
            }else{
                JOptionPane.showMessageDialog(null, "Matricula ou senha inv√°lida");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Por favor preencher os dados ");
        }


    }//GEN-LAST:event_btEntrarActionPerformed

